package poly.dto;

public class DailyDTO {
	private String sal_no;
	private String sal_date;
	private String sal_item;
	private String sal_price;
	private String sal_quantity;
	private String sal_kind;
	
	public String getSal_no() {
		return sal_no;
	}
	public void setSal_no(String sal_no) {
		this.sal_no = sal_no;
	}
	public String getSal_date() {
		return sal_date;
	}
	public void setSal_date(String sal_date) {
		this.sal_date = sal_date;
	}
	public String getSal_item() {
		return sal_item;
	}
	public void setSal_item(String sal_item) {
		this.sal_item = sal_item;
	}
	public String getSal_price() {
		return sal_price;
	}
	public void setSal_price(String sal_price) {
		this.sal_price = sal_price;
	}
	public String getSal_quantity() {
		return sal_quantity;
	}
	public void setSal_quantity(String sal_quantity) {
		this.sal_quantity = sal_quantity;
	}
	public String getSal_kind() {
		return sal_kind;
	}
	public void setSal_kind(String sal_kind) {
		this.sal_kind = sal_kind;
	}
	
	
}